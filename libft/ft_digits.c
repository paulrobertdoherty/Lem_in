#include "libft.h"

int	ft_digits(int n)
{
	int d;

	if (n == -2147483648)
		return (10);
	if (n == 0)
		return (0);
	d = 0;
	if (n < 0)
		n *= -1;
	while (n > 0)
	{
		d++;
		n /= 10;
	}
	return (d);
}
